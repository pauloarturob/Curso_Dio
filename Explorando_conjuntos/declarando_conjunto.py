numeros = set([1, 2, 3, 4, 5])
print(numeros)  # {1, 2, 3, 4, 5}

letras = set('Abacaxi')
print(letras) # {'A', 'x', 'a', 'c', 'i', 'b'}

carros = set(('palio', 'gol', 'celta', 'palio'))
print(carros) # {'gol', 'palio', 'celta'}

linguagens = {'python', 'java', 'python'}
print(linguagens) # {'java', 'python'}
